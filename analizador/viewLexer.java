    private void btnAnalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnalizarActionPerformed
        // TODO add your handling code here:
        File archivo = new File("archivo.txt");
        PrintWriter escribir;
        
        try {
            escribir = new PrintWriter(archivo);
            escribir.print(textoEntrada.getText());
            escribir.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(viewLexer.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Reader lector = new BufferedReader(new StringReader(textoEntrada.getText()));
        Lexer lexer = new Lexer(lector);
        String resultado = "";
        while (true) {
            try {
                Tokens tokens = lexer.yylex();
                if (tokens == null){
                    resultado += "FIN";
                    textoSalida.setText(resultado);
                    return;
                    
                }
                
                switch (tokens) {
                    case ERROR:
                        resultado += "Simbolo no definido\n";
                        
                        break;
                    case ParentesisAbertura:
                    case ParentesisCierre:
                    case Reservadas:    
                        resultado += lexer.lexeme + ": Es " + tokens + "\n";
                        
                        break;
                        
                    default:
                        throw new AssertionError();
                }
            } catch (IOException ex) {
                Logger.getLogger(viewLexer.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
        
        
    }//GEN-LAST:event_btnAnalizarActionPerformed

